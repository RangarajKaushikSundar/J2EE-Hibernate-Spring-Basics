package com.db.example;

public class Employee {
		private int empno;
		private String ename;
		private String job;
		public void setEmpno(int empno) {
			this.empno = empno;
		}
		public int getEmpno() {
			return empno;
		}
		public void setEname(String ename) {
			this.ename = ename;
		}
		public String getEname() {
			return ename;
		}
		public void setJob(String job) {
			this.job = job;
		}
		public String getJob() {
			return job;
		}
}
